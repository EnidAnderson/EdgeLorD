// --- SemanticPatch Data Model ---

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct SemanticPatch {
    // Unique identifier for this patch, derived deterministically from its content.
    // E.g., SHA256 of canonical serialization.
    pub id: HashValue,
    // Protocol version for forward/backward compatibility
    pub version: u32,
    // The kind of semantic transformation applied (e.g., "IntroBinding", "ApplyLemma", "FillHole")
    pub kind: PatchKind,
    // The target in the document (e.g., a specific goal, hole, or span).
    pub target_anchor: StableAnchor,
    // List of semantic diffs, ordered deterministically.
    pub changes: BoundedList<SemanticChange>,
    // Optional: new subgoals generated by the tactic, ordered deterministically.
    pub new_subgoals: BoundedList<Goal>,
    // Optional: UI metadata for LSP, not affecting semantic content
    pub ui_metadata: Option<UIMetadata>,
    // Proof of soundness if applicable and already checked (e.g., a hash of a certified proof term).
    pub soundness_proof_artifact: Option<HashValue>,
}

impl SemanticPatch {
    /// Computes a deterministic hash for the patch by canonical serialization.
    /// Uses `codeswitch::fingerprint::HashValue::hash_with_domain`.
    pub fn compute_id(&self) -> HashValue {
        let mut patch_clone = self.clone();
        patch_clone.id = HashValue::zero(); // Clear ID to prevent circular dependency in hash
        let serialized =
            serde_json::to_string(&patch_clone).expect("Failed to serialize patch for hashing");
        HashValue::hash_with_domain(b"EDGELORD_SEMANTIC_PATCH_V1", serialized.as_bytes())
    }
}

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum PatchKind {
    IntroBinding {
        name: String,
        type_expr: String,
    },
    FillHole {
        hole_id: String,
        term: String, // term is a Mac Lane surface form string
    },
    ApplyRule {
        rule_id: String,
        instantiation: BTreeMap<String, String>, // Rule name and instantiation of metavars
    },
    Rewrite {
        rule_id: String,
        direction: RewriteDirection,
        target_span: ByteSpan,
    },
    // Add other tactic-specific patch kinds as needed
}

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum SemanticChange {
    InsertForm {
        parent_anchor: StableAnchor,
        position: InsertPosition,
        new_form_text: String, // Mac Lane surface form text to insert
    },
    DeleteForm {
        target_anchor: StableAnchor,
    },
    UpdateForm {
        target_anchor: StableAnchor,
        new_form_text: String, // Mac Lane surface form text to update with
    },
}

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum RewriteDirection {
    LeftToRight,
    RightToLeft,
    Bidirectional,
}

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum InsertPosition {
    Prepend,
    Append,
    Before(StableAnchor),
    After(StableAnchor),
}

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct Goal {
    pub id: String, // Deterministic ID for the goal
    pub anchor: StableAnchor, // Location in the document that implies this goal
    pub statement: String, // Mac Lane surface form of the goal statement
    pub context: BoundedList<ContextItem>, // Relevant context items
}

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum ContextItem {
    Binding { name: String, type_expr: String },
    // ... other context items like hypotheses, definitions
}

#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct UIMetadata {
    pub title: String,
    pub description: Option<String>,
    pub icon: Option<String>,
}

// --- Tactic Input Data Model ---

#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct TacticInput {
    pub current_proof_state: ProofState,
    pub target_hole_id: Option<String>,
    pub target_goal_id: Option<String>,
    pub additional_args: BTreeMap<String, String>,
    pub current_document_snapshot_hash: HashValue,
    pub current_compile_options_hash: HashValue,
}

#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct ProofState {
    pub goals: BoundedList<Goal>,
    pub holes: BoundedList<Hole>,
    pub definitions_in_scope: BoundedList<StableAnchor>,
    pub rules_in_scope: BoundedList<StableAnchor>,
    pub current_focus_anchor: StableAnchor,
    pub workspace_snapshot_hash: HashValue,
}

#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct Hole {
    pub id: String,
    pub anchor: StableAnchor,
    pub expected_type: Option<String>, // Mac Lane surface form
    pub context: BoundedList<ContextItem>,
}

// --- Tactic Output Data Model ---

#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum TacticResult {
    Success {
        patches: BoundedList<SemanticPatch>,
        alternatives: BoundedList<SemanticPatch>,
        ui_metadata: Option<UIMetadata>,
    },
    Failure {
        reason: TacticFailureReason,
        message: String,
        // In a real LSP context, this would be an actual lsp_types::Diagnostic
        diagnostic: Option<String>,
    },
    InProgress {
        message: String,
        progress_token: String,
    },
}

#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum TacticFailureReason {
    InputMismatch,
    ContextMismatch,
    FuelExhausted,
    InternalError,
    SoundnessCheckFailed,
    NoAlternativeFound,
    // ...
}

